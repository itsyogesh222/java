package Final;

public class Product {

	

		  int id;
		    String name;
		    double costPrice;
		    double sellPrice;
		    int quantity;

		    // Constructor
		    public Product(int id, String name, double costPrice, double sellPrice, int quantity) {
		        this.id = id;
		        this.name = name;
		        this.costPrice = costPrice;
		        this.sellPrice = sellPrice;
		        this.quantity = quantity;

    }

	}
	

